package com.ericsson.vxmlunit.exception;

public class VXMLScriptException extends Exception {

	public VXMLScriptException() {
		super();
		
	}

	public VXMLScriptException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public VXMLScriptException(String arg0) {
		super(arg0);
		
	}

	public VXMLScriptException(Throwable arg0) {
		super(arg0);
		
	}

}
