package com.ericsson.vxmlunit.exception;

public class VXMLException extends Exception {

	public VXMLException() {
		super();
		
	}

	public VXMLException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public VXMLException(String arg0) {
		super(arg0);
		
	}

	public VXMLException(Throwable arg0) {
		super(arg0);
		
	}

}
